def get_status_by_games(games: int, lang: str = "UA") -> str:
    table = STATUSES_UA if lang == "UA" else STATUSES
    result = table[0][1]
    for threshold, name in table:
        if games >= threshold:
            result = name
    return result


def t(key: str, lang: str, **kwargs) -> str:
    text = TEXTS.get(key, {}).get(lang, TEXTS.get(key, {}).get("UA", key))
    if kwargs:
        try:
            text = text.format(**kwargs)
        except Exception:
            pass
    return text


